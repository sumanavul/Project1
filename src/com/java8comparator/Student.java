package com.java8comparator;

public class Student {
   int roolno;
   String name;
   int age;
public Student(int roolno, String name, int age) {
	super();
	this.roolno = roolno;
	this.name = name;
	this.age = age;
}
public int getRoolno() {
	return roolno;
}
public void setRoolno(int roolno) {
	this.roolno = roolno;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
   
}
